package com.Java;
// Type casting practice
// random declarations
public class TypeCasting {
    public static void main(String[] args) {
        int no = (int)67.7;
        System.out.println(no);

        int number = 'A';
        System.out.println(number);

        System.out.println("नमस्ते"); //Unicode

        byte b = 10;
        byte c = 20;
        int d = b*c;

        System.out.println(d);

        float f = 10.33f;
        double x = f;
        System.out.println(f);

    }
}
